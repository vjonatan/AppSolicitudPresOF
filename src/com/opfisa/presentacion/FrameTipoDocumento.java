    /**
     * Inhabilita los campos y botones.
     */
    void inhabilitar() {
        campoCodigoTD.setEnabled(false);
        campoDescripcionTD.setEnabled(false);
                
        btnGuardar.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnEliminar.setEnabled(false);

    }
    
    /**
     * Habilita los campos y botones.
     */
    void habilitar() {
        if (accion == "editar"){
            campoCodigoTD.setEnabled(false);
        }else{
            campoCodigoTD.setEnabled(true); 
        }
        
        campoDescripcionTD.setEnabled(true);

        btnNuevo.setEnabled(false);
        btnGuardar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }
    
    /**
     * Limpia el formulario para cuando necesitemos ingresar un Tipo Documento.
     */
