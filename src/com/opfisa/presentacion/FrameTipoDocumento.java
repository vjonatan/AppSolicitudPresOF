    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!campoCodigoTD.equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "¿Está seguro de eliminar el Tipo Documento " + campoDescripcionTD.getText() +"?", "Confirmar", 2);

            if (confirmacion == 0) {
                TipoDocumento tipoDocumento = new TipoDocumento();
                ManagerTipoDocumento mTipoDocumento = new ManagerTipoDocumento();

                tipoDocumento.setCodTipoDocumento(Integer.parseInt(campoCodigoTD.getText()));

                if (mTipoDocumento.eliminar(tipoDocumento)) {
                    JOptionPane.showMessageDialog(rootPane, "Tipo Documento eliminado exitosamente");
                    mostrar("");
                    inhabilitar();
                }
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

