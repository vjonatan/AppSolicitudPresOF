    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (campoCodigoTD.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un código para el Tipo de Documento.");
            campoCodigoTD.requestFocus();
            return;
        }

        if (campoDescripcionTD.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una descripción para el Tipo de Documento.");
            campoDescripcionTD.requestFocus();
            return;
        }

        
        TipoDocumento tipoDocumento = new TipoDocumento();
        ManagerTipoDocumento mTipoDocumento = new ManagerTipoDocumento();
        
        tipoDocumento.setCodTipoDocumento(Integer.parseInt(campoCodigoTD.getText()));
        
        tipoDocumento.setDescripcionTipoDocumento(campoDescripcionTD.getText());

        if (accion.equals("guardar")) {
            if (mTipoDocumento.insertar(tipoDocumento)) {
                JOptionPane.showMessageDialog(rootPane, "Tipo Documento registrado Exitosamente");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            if (mTipoDocumento.editar(tipoDocumento)) {
                //limpiarTabla();
                JOptionPane.showMessageDialog(rootPane, "Tipo Documento editado Exitosamente");
                mostrar("");
                inhabilitar();
            }
        }
        
        this.limpiarFormulario();
    }//GEN-LAST:event_btnGuardarActionPerformed

