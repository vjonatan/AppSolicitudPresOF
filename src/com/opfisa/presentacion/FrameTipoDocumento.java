    private void tablaListadoTipoDocumentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaListadoTipoDocumentosMouseClicked
        // TODO add your handling code here:
        accion = "editar";
        btnGuardar.setText("Editar");
        habilitar();
        btnEliminar.setEnabled(true);

        int fila = tablaListadoTipoDocumentos.rowAtPoint(evt.getPoint());

        if (tablaListadoTipoDocumentos.getValueAt(fila, 0).toString() != null) {
            String codTD = tablaListadoTipoDocumentos.getValueAt(fila, 0).toString();
            Double codTDAux = Double.valueOf(codTD);
            Integer codTDAux2 = codTDAux.intValue();
            campoCodigoTD.setText(codTDAux2.toString());
        }

        if (tablaListadoTipoDocumentos.getValueAt(fila, 1).toString() != null) {
            campoDescripcionTD.setText(tablaListadoTipoDocumentos.getValueAt(fila, 1).toString());
        }
    }//GEN-LAST:event_tablaListadoTipoDocumentosMouseClicked

